//floor function in c rounds down the given number
#include <stdio.h>
#include <math.h>
int main(){
    float  a = 27.3;
    float b = 17.5;
    float c = 17.6;
    float d = 7.9;
    
    printf("%f\n", floor(a));
    printf("%f\n", floor(b));
    printf("%f\n", floor(c));
    printf("%f\n", floor(d));

    return 0;
}